// シャッフル(配列を複製して返す)
function shuffled(arr, seed) {
  const a = arr.map((x, i) => [x, i]);
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor((Math.sin(seed * 999 + i) * 10000 % 1 + 1) % 1 * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a.map((x) => x[0]);
}

